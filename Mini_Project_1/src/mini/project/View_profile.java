    private void display(){
    try {
        String Username=jLabel3.getText();
        String sql="Select * from User_info where username=?";
        PreparedStatement st;
        ResultSet rs;
        
        st=DBConnection.getConnection().prepareStatement(sql);
        st.setString(1, Username);
        
        rs=st.executeQuery();
        
        if(rs.next())
        {
            String name=rs.getString("user_name");
            Name.setText(name);
            String gen=rs.getString("gender");
            Sex.setText(gen);
            String course=rs.getString("course");
            Course.setText(course);
            String Per=rs.getString("percentage");
            percent.setText(Per);
            String c_no=rs.getString("Mob_no");
            C_no.setText(c_no);
            String dob=rs.getString("DOB");
            DOB.setText(dob);
            String Fname=rs.getString("Father_name");
            FName.setText(Fname);
            String Email=rs.getString("Email");
            email.setText(Email);
            String Quali=rs.getString("qualifi");
            quali.setText(Quali);
            String Univ=rs.getString("University");
            univ.setText(Univ);
            String Year=rs.getString("year_graduation");
            year.setText(Year);
            String Exp=rs.getString("experience");
            exp.setText(Exp);
        }
    } catch (SQLException ex) {
        Logger.getLogger(View_profile.class.getName()).log(Level.SEVERE, null, ex);
    }
        
        
    }

