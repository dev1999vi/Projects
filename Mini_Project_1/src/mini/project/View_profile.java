    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String contact = C_no.getText();
        String Email=email.getText();
        String Exp=exp.getText();
        String Uname=jLabel3.getText();
        
        String Query="Update user_info set Mob_No=? , Email=?,experience=? where username=?;";
        PreparedStatement st1;
        ResultSet rs1;
        
    try {
        st1=DBConnection.getConnection().prepareStatement(Query);
        
        st1.setString(1, contact);
        st1.setString(2, Email);
        st1.setString(3, Exp);
        st1.setString(4, Uname);
        
        if(st1.executeUpdate()!=0)
        {
            JOptionPane.showMessageDialog(null, "Your Account has been updated successfully.");
        
        }
        else
                    {
                        JOptionPane.showMessageDialog(null, "Error!");
                    }

            } catch (SQLException ex) {
        Logger.getLogger(View_profile.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    C_no.setEditable(false);
        email.setEditable(false);
        exp.setEditable(false);
        
       
    }//GEN-LAST:event_jButton2ActionPerformed

